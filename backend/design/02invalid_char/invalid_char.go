func ReplaceInvalidChar(input string, replaceWith string) (output string, hit bool) {

	if replaceWith == "RANDOM" {
		replaceWith = utils.RandStr(2)
	}

	newPath := input
	for k := range invalidCharMap {
		newPath = strings.ReplaceAll(newPath, k, replaceWith)
		if newPath != input {
			hit = true
		}
	}

	output = newPath

	return
}
